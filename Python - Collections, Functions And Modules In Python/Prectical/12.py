# 12.Write a Python program to access values using dictionary keys.

# --- 1. Create a dictionary with 6 key-value pairs ---
test = {
    "name": "Hardik",
    "age": 21,
    "course": "Python",
    "city": "Surat",
    "grade": "A",
    "marks": 89
}

print("Dictionary:", test)

# Access values using dictionary key
print("Name:", test["name"])
print("Age:", test["age"])
print("Course:", test["course"])
print("City:", test["city"])
print("Grade:", test["grade"])
print("Marks:", test["marks"])

