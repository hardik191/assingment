# 6. Practical Example 5: Write a Python program to find greater and less than a number using
# if_else.

# Program to check if a number is greater or less than another number
num = int(input("Enter a number: "))

# Using if-else condition
if num > 15:
    print("The number is greater than 50.")
else:
    print("The number is less than or equal to 50.")
