# 13.Write a Python program to update a value in a dictionary.

# Update a value in a dictionary 
student = {
    "name": "Hardik",
    "age": 21,
    "course": "Python",
    "marks": 85
}

print("Original Dictionary:", student)

# Update the value of 'marks'
student["marks"] = 92
print("After updating marks:", student)
