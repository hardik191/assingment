# 5. How to create variables in Python?

# String
name = "Hardik"

# Integer
age = 21

# Float
height = 5.9

# Boolean
is_student = True

# List
subjects = ["Python", "DBMS", "Networking"]

# Tuple
grades = (85, 90, 88)

# Dictionary
student_info = {"name": "Hardik", "age": 21, "course": "BCA"}

# Displaying all variable values and types

print("Name:", name, " , Data Type:", type(name))
print("Age:", age, " , Data Type:", type(age))
print("Height:", height, " , Data Type:", type(height))
print("Is Student:", is_student, " , Data Type:", type(is_student))
print("Subjects:", subjects, " , Data Type:", type(subjects))
print("Grades:", grades, " , Data Type:", type(grades))
print("Student Info:", student_info, " , Data Type:", type(student_info))
