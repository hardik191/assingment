# 28. Write a Python program to demonstrate string slicing.
text = "Ranghediya"
print("Original String:", text)
print("First character:", text[0])
print("Last character:", text[-1])
print("From second character onwards:", text[1:])
print("Up to fifth character:", text[:5])
print("Substring from index 1 to 4:", text[1:5])
print("Every alternate character:", text[::2])